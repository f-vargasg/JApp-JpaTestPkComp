    private CuentaId validarCuentaId(String idClienteStr, String idCuentaStr) {

        CuentaId cuentaId = null;
        int idClienteN = 0;
        int idCuentaN = 0;
        boolean clienteValido;
        boolean cuentaValida = false;
        // validar cliente
        clienteValido = MyDouble.isNumeric(idClienteStr);
        // validar cuenta
        if (idCuentaStr.length() > 0) {
            if (MyDouble.isNumeric(idCuentaStr)) {
                idCuentaN = Integer.parseInt(idCuentaStr);
                cuentaValida = true;
            }
        } else {
            idCuentaN = 0;
            cuentaValida = true;
        }

        if (clienteValido && cuentaValida) {
            idClienteN = Integer.parseInt(idClienteStr);
            cuentaId = new CuentaId(idClienteN, idCuentaN);
        }
        return cuentaId;
    }

    private void jBtnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOkActionPerformed
        switch (this.frmStatus) {
            case FRMINSERT -> {
                CuentaId cuentaId = validarCuentaId(jTxtFldIdClienteN.getText(), jTxtFldIdCuentaN.getText());
                if (cuentaId != null) {
                    int idClienteN = Integer.parseInt(jTxtFldIdClienteN.getText());
                    Cliente cliente = clienteBl.buscarPorId(idClienteN);
                    cuenta = new Cuenta(cuentaId, jTxtFldNomCuenta.getText(), cliente);
                    // Guarda en la BD
                    cuentaBl.crear(cuenta);
                    MySwingUtil.mostrarMensaje("Cuenta ingresada correctamente", MySwingUtil.TD_INFO,
                            "Ingreso de Cuentas");
                } else {
                    MySwingUtil.mostrarMensaje("Numero de cuenta invalida, revisar", MySwingUtil.TD_ERROR,
                            "Ingreso de Cuentas");
                }
            } 

            case FRMUPDATE -> {
                   cuenta.setNomCuenta(jTxtFldNomCuenta.getText() );
                   cuentaBl.modifcar(cuenta);
                   MySwingUtil.mostrarMensaje("Cuenta modificada correctamente", MySwingUtil.TD_INFO, 
                                                                  "Modificaci√≥n de Cuentas");
            }
        }
        this.dispose();
    }//GEN-LAST:event_jBtnOkActionPerformed

